def add(a,b):
  adds = a + b
  return adds
def subtract(a,b):
  subtracts = a - b
  return subtracts
def multiply(a,b):
  multi = a*b
  return multi
def divide(a,b):
  div = a/b
  return div
def expo(a,b):
  exp = a ** b
  return exp
print(add(5,4))
print(subtract(7,6))
print(multiply(5,4))
print(divide(4,2))
print(expo(4,3))
